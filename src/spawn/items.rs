use crate::{
    AreaOfEffect, Confusion, Consumable, DefenseBonus, EquipmentSlot, Equippable, InflictsDamage,
    Item, MeleePowerBonus, Name, Position, ProvidesHealing, Ranged, Renderable, SerializeMe,
};
use rltk::RGB;
use specs::{
    prelude::*,
    saveload::{MarkedBuilder, SimpleMarker},
};

/// Spawns a Confusion scroll at a given location
pub fn confusion_scroll(ecs: &mut World, x: i32, y: i32) {
    ecs.create_entity()
        .with(Position { x, y })
        .with(Renderable {
            glyph: rltk::to_cp437(')'),
            fg: RGB::named(rltk::PINK),
            bg: RGB::named(rltk::BLACK),
            render_order: 2,
        })
        .with(Name {
            name: "Confusion Scroll".to_string(),
        })
        .with(Item {})
        .with(Consumable {})
        .with(Ranged { range: 6 })
        .with(Confusion { turns: 4 })
        .marked::<SimpleMarker<SerializeMe>>()
        .build();
}

